public class Loops {
    
    public static void main(String[] args) {
        
        for (int i = 0; i <= 100; i++) {
            System.out.println(Math.pow(i, 1));
        }

        int x = 0;
        while(x < 100) {
            System.out.println("Not yet");
            x++;
        }
    }
}
